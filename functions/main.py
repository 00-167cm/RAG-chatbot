@functions_framework.http
def reset_chats(request):
    """
    HTTPリクエストで呼び出されるリセット関数

    Cloud Schedulerから毎日22時に呼び出される
    1. Firestoreの全チャットを削除
    2. テンプレート会話を新規作成
    """
    now = datetime.now(JST)
    print(f"🔄 リセット開始: {now.strftime('%Y-%m-%d %H:%M:%S')}")

    try:
        chats_ref = db.collection('chats')

        # ============================================
        # Step 1: 全チャットを削除
        # ============================================
        docs = chats_ref.get()
        delete_count = 0

        for doc in docs:
            doc.reference.delete()
            delete_count += 1
            print(f"   ✅ 削除: {doc.id}")

        print(f"📋 {delete_count}件のチャットを削除しました")

        # ============================================
        # Step 2: テンプレート会話を作成
        # ============================================
        create_count = 0

        for template in TEMPLATE_CHATS:
            chat_data = {
                "title": template["title"],
                "messages": template["messages"],
                "created_at": now,
                "updated_at": now
            }

            doc_ref = chats_ref.add(chat_data)
            create_count += 1
            print(f"   ✅ 作成: {template['title']}")

        print(f"📝 {create_count}件のテンプレートを作成しました")

        # ============================================
        # 完了レスポンス
        # ============================================
        result_message = (
            f"✅ デモリセット完了！\n"
            f"実行日時: {now.strftime('%Y-%m-%d %H:%M:%S')} (JST)\n"
            f"削除数: {delete_count}件\n"
            f"作成数: {create_count}件"
        )

        print(result_message)
        return result_message, 200

    except Exception as e:
        error_message = f"❌ エラー発生: {str(e)}"
        print(error_message)
        return error_message, 500